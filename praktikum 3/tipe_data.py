print("Data type in python")

print("String")
a = "hello"
print(type(a))

print("Integer")
b = 20
print(type(b))

print("Float")
c = 10.5
print(type(c))

print("complex")
d = 1j
print(type(d))

print("list")
e = ["a","b","c"]
print(type(e))

print("tuple")
f = ("a","b","c")
print(type(f))

print("Range")
g = range(5)
print(type(g))

print("dict")
h = {"nama" : "Basim", "umur" : 33}
print(type(h))

print("set")
i = {"Basim", "Hytam", "Bayek"}
print(type(i))

print("Frozenset")
j = frozenset(["basim", "Hytam","Eivor"])
print(type(j))

print("bool")
k = True
print(type(k))

print("bytes")
l = b"hello"
print(type(l))

print("byte array")
m = bytearray(5)
print(type(m))

print("Memory View")
n = memoryview(bytes(5))
print(type(n))

print("None Type")
o = None
print(type(o))